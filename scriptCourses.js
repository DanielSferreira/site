function btn_action(id, course) {
    try {
        document.querySelector(`#${id}`).onclick = () => {
            let modal = document.querySelector('#modal-container')
            modal.classList.add("show-modal");
            modal.appendChild(AppendModalContent(achar(course)));
            modal.classList.remove("hide-modal");
        };
    } catch (e) {
    }
}

function achar(param) {
    try {
        return arr.find((a) => a.curso.includes(param));
    } catch (e) {
    }
}

function fechar_modal() {
    let caixa = document.querySelector('#modal-cotent');
    let modal = document.querySelector('#modal-container')

    caixa.classList.remove("animate__fadeInDown");
    caixa.style.setProperty('--animate-duration', '0.3s');
    caixa.classList.add("animate__fadeOutUp");

    setTimeout(function () {
        modal.classList.add("hide-modal");
        modal.innerHTML = '';
        modal.classList.remove("show-modal");
    }, 200)
}